fn main() {


    // tipos de datos

    // i8, i16, i32, i64, i128 -> con signo - +
    // u8, u16, u32, u64, u128 -> Sin signo +

    let numero_uno: i8 = -10;

    let numero_dos: u8 = 20;

    // char -> UTF-8
    let carater = 'a';

    // float
    let real: f32 = 12.5;

    let resultado: bool = true; // false

println!("{} {} {} {} {}", numero_uno, numero_dos, carater, real, resultado);
}
